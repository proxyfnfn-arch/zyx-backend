// Estado de uso en memoria
const usedCodes = new Map(); // code -> { usedBy, usedAt }

function getCodePlan(code) {
  if (PRO_CODES.includes(code))   return 'pro';
  if (ULTRA_CODES.includes(code)) return 'ultra';
  return null;
}

// POST /api/redeem  { code }
router.post('/', auth, (req, res) => {
  const code = (req.body.code || '').trim().toUpperCase();
  if (!code) return res.status(400).json({ error: 'Introduce un código' });

  const plan = getCodePlan(code);
  if (!plan) return res.status(400).json({ error: 'Código inválido. Comprueba que esté bien escrito.' });

  if (usedCodes.has(code)) {
    const info = usedCodes.get(code);
    if (info.usedBy === req.user.id) return res.status(400).json({ error: 'Ya usaste este código.' });
    return res.status(400).json({ error: 'Este código ya ha sido canjeado por otro usuario.' });
  }

  const user = findUserById(req.user.id);
  if (!user) return res.status(404).json({ error: 'Usuario no encontrado' });

  if (user.plan === plan) return res.status(400).json({ error: `Ya tienes el plan ${plan.toUpperCase()} activo.` });

  // Marcar como usado
  usedCodes.set(code, { usedBy: req.user.id, usedAt: new Date() });

  // Activar plan 30 días
  user.plan = plan;
  const exp = new Date();
  exp.setDate(exp.getDate() + 30);
  user.planExpiresAt = exp;

  res.json({
    message: `✅ Plan ${plan.toUpperCase()} activado correctamente. Válido 30 días.`,
    plan,
    expiresAt: user.planExpiresAt
  });
});

// GET /api/redeem/status (admin) — ver códigos usados
router.get('/status', auth, adminOnly, (req, res) => {
  const used = Array.from(usedCodes.entries()).map(([code, v]) => ({ code, ...v }));
  res.json({ usedCodes: used, total: used.length });
});

